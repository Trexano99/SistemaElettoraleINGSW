package useObject.baseElements;

import java.sql.Date;

public class Candidato {
	
	private int id;
	private String nome, cognome;
	private Date dataNascita;
	
	
	public int getId() {
		return id;
	}
	public String getNome() {
		return nome;
	}
	public String getCognome() {
		return cognome;
	}
	public Date getDataNascita() {
		return dataNascita;
	}

	
	
}
